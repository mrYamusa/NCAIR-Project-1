# Print two ASCII art side by side (Function). Takes the ASCII art as arguments and the distance to be placed between them
def print_ascii_side_by_side(art1, art2, width=63):
    # Split the ASCII art strings into lines
    arts1 = art1.split('\n')
    arts2 = art2.split('\n')

    # Print side by side
    for line1, line2 in zip(arts1, arts2):
        print(line1.ljust(width) + line2)


